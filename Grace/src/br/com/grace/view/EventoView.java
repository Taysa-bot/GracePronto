    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTableCadastroEvento.getModel();
        modelo.setNumRows(0);
        EventoDao fdao = new EventoDao();

        for (Evento f : fdao.read()) {
            modelo.addRow(new Object[]{
                f.getId(),
                f.getTipo(),
                f.getNome(),
                f.getData(),
                f.getHora(),
                f.getIdDisciplina(),
                f.getFrequencia(),
                f.getStatus(),
                f.getAssunto()
            });
        }

    }

