    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        String tipo = (String) jComboBoxTipo.getSelectedItem();
        String nome = jTextFieldNome.getText();
        String data = jFormattedTextFieldData.getText();
        String hora = jFormattedTextFieldHora.getText();
        String frequencia = (String) jComboBoxFrequencia.getSelectedItem();
        String status = (String) jComboBoxStatus.getSelectedItem();
        String assunto = jTextFieldAssunto.getText();

        Disciplina disciplina = (Disciplina) jComboBoxDisciplina.getSelectedItem();

        Evento f = new Evento(tipo, nome, data, hora, disciplina.getId(), frequencia, status, assunto);

        EventoDao fd = new EventoDao();
        fd.create(f);
        readJTable();

        this.jComboBoxTipo.setToolTipText("");
        this.jTextFieldNome.setText("");
        this.jFormattedTextFieldData.setText("");
        this.jFormattedTextFieldHora.setText("");
        this.jComboBoxDisciplina.setToolTipText("");
        this.jComboBoxFrequencia.setToolTipText("");
        this.jTextFieldAssunto.setText("");

    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (jTableCadastroEvento.getSelectedRow() != -1) {

            Evento f = new Evento("", "", "", "", 0, "", "", "");
            EventoDao dao = new EventoDao();

            f.setId((int) jTableCadastroEvento.getValueAt(jTableCadastroEvento.getSelectedRow(), 0));

            dao.delete(f);
            readJTable();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir");
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        if (jTableCadastroEvento.getSelectedRow() != -1) {
 Disciplina disciplina = (Disciplina) jComboBoxDisciplina.getSelectedItem();
            Evento f = new Evento((String) jComboBoxTipo.getSelectedItem(), jTextFieldNome.getText(), jFormattedTextFieldData.getText(), jFormattedTextFieldHora.getText(), disciplina.getId(), (String) jComboBoxFrequencia.getSelectedItem(), (String) jComboBoxStatus.getSelectedItem(), jTextFieldAssunto.getText());
            EventoDao dao = new EventoDao();
           
            f.setTipo((String) jComboBoxTipo.getSelectedItem());
            f.setNome(jTextFieldNome.getText());
            f.setData(jFormattedTextFieldData.getText());
            f.setStatus((String) jComboBoxStatus.getSelectedItem());
            f.setIdDisciplina(disciplina.getId());
            f.setHora(jFormattedTextFieldHora.getText());
            f.setFrequencia((String) jComboBoxFrequencia.getSelectedItem());
            f.setAssunto(jTextFieldAssunto.getText());
            f.setId(Integer.parseInt(jTextFieldId.getText()));

            dao.update(f);

            this.jComboBoxTipo.setToolTipText("");
            this.jTextFieldNome.setText("");
            this.jFormattedTextFieldData.setText("");
            this.jFormattedTextFieldHora.setText("");
            this.jComboBoxDisciplina.setToolTipText("");
            this.jComboBoxFrequencia.setToolTipText("");
            this.jComboBoxStatus.setToolTipText("");
            this.jTextFieldAssunto.setText("");

            readJTable();
        } else {
            JOptionPane.showMessageDialog(this, "selecione uma linha para atualizar");
        }

    }//GEN-LAST:event_jButtonAtualizarActionPerformed

