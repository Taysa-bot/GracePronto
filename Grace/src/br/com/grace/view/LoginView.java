    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed

        EstudanteDao ed = new EstudanteDao();

        int matricula = Integer.parseInt(jTextFieldId.getText());
        String senha = Arrays.toString(jPasswordFieldSenha.getPassword());

        if (ed.login(matricula,senha)) {
            HomeView f = new HomeView();
            f.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Dados inv√°lidos", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEntrarActionPerformed

